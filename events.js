document.addEventListener('DOMContentLoaded', (event) => {
    let eventsContainer = document.querySelector('.events');

    function parseEventDate(dateString) {
        const [day, month, year] = dateString.split('-').map(num => parseInt(num, 10));
        return new Date(year, month - 1, day);
    }


    // Sort the events array by date in ascending order
events.sort((a, b) => parseEventDate(a.date) - parseEventDate(b.date));

    events.forEach(function(event) {
        let eventDate = parseEventDate(event.date);
        eventDate.setHours(0, 0, 0, 0);
        let comparisonDate = new Date();
        comparisonDate.setHours(0, 0, 0, 0);

        if (eventDate < comparisonDate) {
            return;
        }

        var eventElement = document.createElement('div');
        eventElement.style.padding = '10px';
        eventElement.style.margin = '10px';
        eventElement.style.borderRadius = '10px';
        eventElement.style.backgroundColor = 'rgba(206, 212, 228, 0.3)';
        eventElement.style.color = 'rgb(94, 60, 36)';
        eventElement.style.fontSize = '18px'; // Corrected property name

        var nameElement = document.createElement('p');
        nameElement.textContent = event.name;
        nameElement.style.fontWeight = '700';
        eventElement.appendChild(nameElement);

        var dateElement = document.createElement('p');
        dateElement.textContent = `Date: ${event.date}`;
        eventElement.appendChild(dateElement);

        var textElement = document.createElement('p');
        textElement.textContent = event.text;
        eventElement.appendChild(textElement);

        var linkElement = document.createElement('a');
linkElement.setAttribute('href', event.link);
linkElement.setAttribute('target', '_blank'); // Make the link open in a new window

// Use FontAwesome icon
var iconElement = document.createElement('i');
iconElement.className = 'fas fa-external-link-alt'; // FontAwesome class for the link icon
linkElement.appendChild(iconElement);

var linkText = document.createTextNode(' Skoða betur'); // Add text next to the icon
linkElement.appendChild(linkText);

eventElement.appendChild(linkElement);

        eventsContainer.appendChild(eventElement);
    });
});
